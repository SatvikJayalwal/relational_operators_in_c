#include<stdio.h>
int main()
{
    printf("\nThis program is a Demonstration of Relational operations in C langusge\n");
    int x = 12, y = 13;
    printf("x = %d\n", x);
    printf("y = %d\n\n", y);
    printf("x > y : %d\n", x > y);    
    printf("x >= y : %d\n", x >= y);   
    printf("x < y : %d\n", x < y);   
    printf("x <= y : %d\n", x <= y);    
    printf("x == y : %d\n", x == y);
    printf("x != y : %d\n", x != y);
    return 0;
}